# Works same as in other languages, only minor differences
x = 1
y = 2.0
z = "3"

# Once again, you can math during print statements to change the results
print(x * 3) # 3
print(y * 7) # 14.0
print(z * 4) # 3333
print(x * y) # 2.0

# Doing so doesn't change the actual value in the variable unless you change it beforehand like so
other = 7.1

other = int(other)
print(other) # 7